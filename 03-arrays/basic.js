// 🔹 BASIC ARRAY OPERATIONS

// Creating an array
let arr = [1, 2, 3, 4, 5];

// Accessing elements
console.log(arr[0]); // 1

// Updating value
arr[2] = 10;
console.log(arr); // [1, 2, 10, 4, 5]

// push → add at end
arr.push(6);
console.log(arr);

// pop → remove last
arr.pop();
console.log(arr);

// shift → remove first
arr.shift();
console.log(arr);

// unshift → add at start
arr.unshift(0);
console.log(arr);
